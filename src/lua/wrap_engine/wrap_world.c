int luaopen_world ( lua_State *_l ) {
    static const luaL_Reg __functions[] = {
        // { "log", lua_ex_log },
        { 0, 0 }
    };

    // we create global ex table if it not exists.
    ex_lua_global_module ( _l, "ex" );
    luaL_register( _l, 0, __functions );

    lua_pop(_l, 1); // pops ex

    return 0;
}

