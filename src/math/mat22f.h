typedef struct mat22f_t {
    union {
        struct { 
            float m00, m01;
            float m10, m11;
        }; // end struct
        float m[4];
    };
} mat22f_t;

// ------------------------------------------------------------------ 
// Desc: 
// ------------------------------------------------------------------ 

inline void mat22f_zero ( mat22f_t* _m ) { 
    _m->m00 = 0.0f, _m->m01 = 0.0f; 
    _m->m10 = 0.0f, _m->m11 = 0.0f; 
}
inline void mat22f_identity ( mat22f_t* _m ) { 
    _m->m00 = 1.0f, _m->m01 = 0.0f; 
    _m->m10 = 0.0f, _m->m11 = 1.0f; 
}

