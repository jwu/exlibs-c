// ------------------------------------------------------------------ 
// Desc: 
// ------------------------------------------------------------------ 

int main( int argc, char* argv[] )
{
    // init
    if ( core_init() ) {

        // ogre app
        {
            // init ogre
            App app;
            if ( app.init() ) {

                // main loop
                while ( app.window()->isClosed() == false ) {
                    app.tick();
                }

                // deinit ogre
                app.deinit();
            }
        }

        // deinit
        core_deinit();
    }

    return 0;
}
    
